# Q-13. W.A.P to perform all arithmetical operations on two float variables in Python

x = 20.50
y = 10.50

print("Perform all arithmetical operation (on two float variable)")

print("Addition :",x+y)
print("Substraction :",x-y)
print("Multiplication :",x*y)
print("Division :",x/y)
print("Floor Division :",x//y)
print("Modulus :",x%y)